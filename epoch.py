def build_fusion_model_safe(num_classes=8):


    a_inp = layers.Input(shape=(128, 128, 1), name='audio_input')
    x = layers.Conv2D(32, (3,3), padding='same', use_bias=False)(a_inp)
    x = layers.BatchNormalization()(x); x = layers.Activation('relu')(x)
    x = layers.Conv2D(32, (3,3), padding='same', use_bias=False)(x)
    x = layers.BatchNormalization()(x); x = layers.Activation('relu')(x)
    x = layers.MaxPooling2D((2,2))(x); x = layers.Dropout(0.2)(x)

    x = layers.Conv2D(64, (3,3), padding='same', use_bias=False)(x)
    x = layers.BatchNormalization()(x); x = layers.Activation('relu')(x)
    x = layers.Conv2D(64, (3,3), padding='same', use_bias=False)(x)
    x = layers.BatchNormalization()(x); x = layers.Activation('relu')(x)
    x = layers.MaxPooling2D((2,2))(x); x = layers.Dropout(0.25)(x)

    x = layers.Conv2D(128, (3,3), padding='same', use_bias=False)(x)
    x = layers.BatchNormalization()(x); x = layers.Activation('relu')(x)
    x = layers.Conv2D(128, (3,3), padding='same', use_bias=False)(x)
    x = layers.BatchNormalization()(x); x = layers.Activation('relu')(x)
    x = layers.MaxPooling2D((2,2))(x); x = layers.Dropout(0.3)(x)

    avg_p = layers.GlobalAveragePooling2D()(x)
    max_p = layers.GlobalMaxPooling2D()(x)
    x = layers.Concatenate()([avg_p, max_p])
    x = layers.Dense(256, activation='relu', kernel_regularizer=regularizers.l2(1e-4))(x)
    x = layers.Dropout(0.4)(x)
    audio_feat = layers.Dense(128, activation='relu', name='audio_bottleneck',
                              kernel_regularizer=regularizers.l2(1e-4))(x)

    t_inp = layers.Input(shape=(40,), name='text_input')
    tx = layers.Embedding(5000, 128, mask_zero=True)(t_inp)
    tx = layers.SpatialDropout1D(0.3)(tx)
    tx = layers.Bidirectional(layers.GRU(128, return_sequences=True, dropout=0.2, recurrent_dropout=0.2))(tx)
    tx = layers.Bidirectional(layers.GRU(64, return_sequences=True, dropout=0.2, recurrent_dropout=0.2))(tx)
    tx = layers.Bidirectional(layers.GRU(32, return_sequences=False, dropout=0.2))(tx)
    tx = layers.Dense(128, activation='relu', kernel_regularizer=regularizers.l2(1e-4))(tx)
    tx = layers.Dropout(0.4)(tx)
    text_feat = layers.Dense(64, activation='relu', name='text_bottleneck',
                             kernel_regularizer=regularizers.l2(1e-4))(tx)

    combined = layers.Concatenate()([audio_feat, text_feat])
    fx = layers.Dense(256, activation='relu', kernel_regularizer=regularizers.l2(1e-4))(combined)
    fx = layers.Dropout(0.4)(fx)
    fx = layers.Dense(128, activation='relu', kernel_regularizer=regularizers.l2(1e-4))(fx)
    fx = layers.Dropout(0.3)(fx)
    out = layers.Dense(num_classes, activation='softmax')(fx)

    model = models.Model(inputs=[a_inp, t_inp], outputs=out, name='fusion_model')
    return model

