def build_text_rnn(vocab_size=5000, embed_dim=128, max_len=40, num_classes=8):
    inp = layers.Input(shape=(max_len,))

    x = layers.Embedding(vocab_size, embed_dim, mask_zero=True)(inp)
    x = layers.SpatialDropout1D(0.3)(x)

    x = layers.Bidirectional(layers.GRU(128, return_sequences=True,
                             dropout=0.2, recurrent_dropout=0.2))(x)
    x = layers.Bidirectional(layers.GRU(64, return_sequences=True,
                             dropout=0.2, recurrent_dropout=0.2))(x)
    x = layers.Bidirectional(layers.GRU(32, return_sequences=False,
                             dropout=0.2))(x)

    x = layers.Dense(128, activation='relu',
                     kernel_regularizer=regularizers.l2(1e-4))(x)
    x = layers.Dropout(0.4)(x)
    features = layers.Dense(64, activation='relu',
                            kernel_regularizer=regularizers.l2(1e-4))(x)
    x = layers.Dropout(0.3)(features)
    out = layers.Dense(num_classes, activation='softmax')(x)

    model = models.Model(inp, out, name='text_rnn')
    return model, features

