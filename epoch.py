def extract_melspectrogram(filepath, sr=22050, n_mels=128, max_len=128,
                           augment=False):
    try:
        y, sr = librosa.load(filepath, sr=sr, duration=3.0)
        target_len = sr * 3
        if len(y) < target_len:
            y = np.pad(y, (0, target_len - len(y)))
        else:
            y = y[:target_len]

        if augment:
            if np.random.rand() < 0.5:
                rate = np.random.uniform(0.85, 1.15)
                y = librosa.effects.time_stretch(y, rate=rate)
            if np.random.rand() < 0.5:
                steps = np.random.randint(-3, 4)
                y = librosa.effects.pitch_shift(y, sr=sr, n_steps=steps)
            if np.random.rand() < 0.3:
                y = y + 0.005 * np.random.randn(len(y))

        if len(y) < target_len:
            y = np.pad(y, (0, target_len - len(y)))
        else:
            y = y[:target_len]

        mel = librosa.feature.melspectrogram(
            y=y, sr=sr, n_mels=n_mels, n_fft=2048, hop_length=512
        )
        mel_db = librosa.power_to_db(mel, ref=np.max)
        mel_db = mel_db[:, :max_len]
        if mel_db.shape[1] < max_len:
            mel_db = np.pad(mel_db, ((0, 0), (0, max_len - mel_db.shape[1])))
        return mel_db
    except Exception as e:
        print(f'Error with {filepath}: {e}')
        return None

